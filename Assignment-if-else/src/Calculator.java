public class Calculator{

    public  float adunare  (float a, float b){
        return a + b;
    }
    public  float impartire(float a, float b){
        return a / b;
    }
    public  float scadere  (float a, float b){ return a - b;}
    public  float inmultire(float a, float b){ return a * b;}
    public  float mediaAritmetica(float a, float b, float c){
        return ((a + b + c) / 3);
    }
}
